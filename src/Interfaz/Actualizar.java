    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int filaSeleccionada = jTable1.rowAtPoint(evt.getPoint());
        jTextField1.setText(jTable1.getValueAt(filaSeleccionada,1).toString());
        jTextField2.setText(jTable1.getValueAt(filaSeleccionada,2).toString());
        jTextField3.setText(jTable1.getValueAt(filaSeleccionada,3).toString());
        jTextField5.setText(jTable1.getValueAt(filaSeleccionada,4).toString());
        jTextField4.setText(jTable1.getValueAt(filaSeleccionada,5).toString());
        jTextField6.setText(jTable1.getValueAt(filaSeleccionada,6).toString());
        switch((String) jTable1.getValueAt(filaSeleccionada,5)){
            case "Short":
                indice = 0;
                break;
            case "Camisa":
                indice = 1;
                break;
            case "Pantalón":
                indice = 2;
                break;
        }
        jComboBox1.setSelectedIndex(indice);
    }//GEN-LAST:event_jTable1MouseClicked

