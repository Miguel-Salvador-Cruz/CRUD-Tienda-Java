    public void seleccion(){
        int filaSeleccionada = jTable1.getSelectedRow();
        String dao = (String) jTable1.getValueAt(filaSeleccionada, 0);
        obj.setMarca(jTextField1.getText());
        obj.setNombre(jTextField2.getText());
        obj.setPrecioCompra(Integer.parseInt(jTextField3.getText()));
        obj.setExistencia(Integer.parseInt(jTextField4.getText()));
        obj.setPrecioVenta(Integer.parseInt(jTextField5.getText()));
        obj.setDescripcion(jTextField6.getText());
        obj.setCategoria(jComboBox1.getSelectedIndex());
        obj.setTipoPrenda(jComboBox1.getItemAt(jComboBox1.getSelectedIndex()));
        obj.setDao(dao);
    }
    
