    public void vaciar(){
        DefaultTableModel v = (DefaultTableModel) jTable1.getModel();
        int a = jTable1.getRowCount()-1;
        for (int i = a; i >= 0; i--) {          
        v.removeRow(v.getRowCount()-1);
        }
    }
    
    public void mostrar(){
        vaciar();
        obj2.obtener();
        jTable1.setModel(obj2.getModelo());
    }
        
    public void seleccion(){
        int filaSeleccionada = jTable1.getSelectedRow();
        String SQL = "delete from prenda where idPrenda = " + jTable1.getValueAt(filaSeleccionada, 0);
        obj.setFilaSeleccionada(SQL);
        String SQL2 = "delete from departamento where idDepartamento = " + jTable1.getValueAt(filaSeleccionada, 0);
        obj.setFilaSeleccionada2(SQL2);
        obj.eliminar();
    }

